#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int a;

	a = n;
	if (a < 0)
	{
		a *= -1;
		ft_putchar('-');
	}
	if (a > 9)
	{
		ft_putnbr(a / 10);
	}
	a = (a % 10) + 48;
	ft_putchar(a);
}
